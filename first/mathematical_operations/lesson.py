# МАТЕМАТИЧЕСКИЕ ОПЕРАЦИИ


# Математические операции:
# Сложение: +
# Вычитание: -
# Произведение: *
# Деление: /
# Целочисленное деление: //
# Остаток от деления: %
# Возведение в степень: **

a, b = int(input('Введите первое число: ')), int(input('Введите второе число: '))
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a**b)

