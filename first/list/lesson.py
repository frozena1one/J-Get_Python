# Задача:
# Создайте два разных списка и свяжите их с переменными a и b.
# Извлеките из первого списка третий элемент.
# Измените во втором списке последний элемент на “!”
# Соедините оба списка в одну переменную.
# Удалите первые три элемента.

# Решение:
first = [0, 1, 2, 3, 4]
second = [5, 6, 7, 8, 9]
print(first[2]) # извлекли из первого списка третий элемент
second[-1] = '!' # изменили во втором списке последний элемент на "!"
print(second)
third = first + second # соединили оба списка в один
print(third)
del third[:3] # удалил первые 3 элемента полученного списка
print(third)

# Задача:
# Создайте список из строки “победа”.
# Напишите ряд действий которые позволили получить список со значениями [обеда, беда, еда, да, а]

# Решение:
letters = list('победа')
index = 1
while index < len(letters):
    print(letters[index:])
    index = index + 1
