# Задача:
# В некоторых аттракционах есть допуск по росту. Если рост менее 120 см, то кататься на аттракционе запрещено.
# Напишите программу, которая запрашивает рост, и если значение меньше допустимого, то выдает сообщение “К сожалению, ваш рост не позволяет”,
# а если значение больше или равно, то выводится сообщение “Хорошо провести Вам время”

# Решение:
height = int(input('Введите ваш рост: '))
if height >= 120:
    print('Хорошо провести Вам время!')
else:
    print('К сожалению, ваш рост не позволяет.')

# Задача:
# Для марширования на парадах подбирают мужчин с определенным ростом. Их рост не должен быть менее 170 см и не более 180 см.
# Напишите программу, которая определяет годность для маршировки на парадах.
# Т.е. вначале вводится значение через input().
# Если это значение больше 170 и меньше 180, то выводится сообщение “годен”, иначе выводится сообщение “не годен”.

# Решение:
# Решим эту задачу двумя способами

# Первый способ
height = int(input('Введите ваш рост: '))
if height > 170 and height < 180:
    print('годен')
else:
    print('не годен')

# Второй способ
height = int(input('Введите ваш рост: '))
if height < 170 or height > 180:
    print('не годен')
else:
    print('годен')

