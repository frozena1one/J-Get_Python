# Task 1:
#    Напишите программу для вывода количества уникальных символов каждого считанного слова
#    без учета регистра.
#    На вход программе в первой строке подается число n – общее количество слов.
#    Далее идут n строк с словами.
#    Программа должна вывести на отдельной строке количество уникальных символов для каждого слова.

# Решение:
n = int(input())
for i in range(n):
    word = input().lower()
    letters = set(word)
    print(len(letters))

# в одну строчку
