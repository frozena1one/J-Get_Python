# Task 2:
#    Напишите программу для вывода общего количества уникальных символов во всех считанных словах
#    без учета регистра.
#    На вход программе в первой строке подается число n – общее количество слов.
#    Далее идут n строк со словами.
#    Программа должна вывести одно число – общее количество уникальных символов во всех словах
#    без учета регистра.

# Решение:
n = int(input())
all_letters = set()
for i in range(n):
    word = input().lower()
    letters = set(word)
    all_letters.add(*letters)
print(len(all_letters))

